<h5>1. Introduction</h5>
<p>
This Privacy Policy explains how we collect, use, store, and protect your personal
information when you use our Human Resource Management (HRM) platform.
We are committed to protecting your privacy and handling your data responsibly.
</p>

<h5>2. Information We Collect</h5>
<p>
We may collect and process the following types of information:
</p>
<ul>
    <li>Personal identification details (name, email address, phone number)</li>
    <li>Employment information (job title, department, salary, attendance records)</li>
    <li>Payroll and financial information (allowances, deductions, tax-related data)</li>
    <li>System usage data (login activity, audit logs, IP addresses)</li>
</ul>

<h5>3. How We Use Your Information</h5>
<p>
Your information is used strictly for legitimate business purposes, including:
</p>
<ul>
    <li>Managing employee records and HR operations</li>
    <li>Processing payroll and generating payslips</li>
    <li>Managing contracts, leave, and performance records</li>
    <li>Ensuring system security, compliance, and auditability</li>
</ul>

<h5>4. Data Storage & Security</h5>
<p>
We implement appropriate technical and organizational measures to protect
your personal data against unauthorized access, loss, or misuse. Access to
sensitive information is restricted to authorized users only.
</p>

<h5>5. Data Sharing</h5>
<p>
We do not sell, trade, or rent personal data to third parties. Data may only be
shared where required by law, regulatory authorities, or with trusted service
providers strictly for system functionality.
</p>

<h5>6. Data Retention</h5>
<p>
Personal data is retained only for as long as necessary to fulfill the purposes
for which it was collected, including legal, accounting, or reporting obligations.
</p>

<h5>7. User Responsibilities</h5>
<p>
Organizations using this platform are responsible for ensuring that employee
data entered into the system is accurate, lawful, and collected with proper
consent where required.
</p>

<h5>8. Your Rights</h5>
<p>
Depending on applicable laws, users may have the right to request access to,
correction of, or deletion of their personal information. Requests should be
directed to the system administrator.
</p>

<h5>9. Changes to This Policy</h5>
<p>
We may update this Privacy Policy from time to time. Any changes will be
published within the system, and continued use of the platform constitutes
acceptance of the updated policy.
</p>

<h5>10. Contact Information</h5>
<p>
If you have questions or concerns about this Privacy Policy or how your data is
handled, please contact your system administrator or the platform provider.
</p>
